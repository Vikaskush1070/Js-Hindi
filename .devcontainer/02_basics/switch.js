const months = 4

switch (months) {
    case 1:
        console.log("Jab");
        break;
    case 2:
        console.log("Feb");
        break;

    case 3:
        console.log("Mae");
        break;

    case 4:
        console.log("Apri");
        break;

    default:
        console.log("valid months");
        break;




}

